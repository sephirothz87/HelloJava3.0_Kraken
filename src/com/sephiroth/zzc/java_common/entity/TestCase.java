package com.sephiroth.zzc.java_common.entity;

public class TestCase {
	public String Name;
	public String Type;
	public String State;
	public String Level;
	public String IsDeveloperSelfTest;
	public String Steps;
	public String Directory;
	public String PreTest;
	public String ExResult;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getType() {
		return Type;
	}

	public void setType(String type) {
		Type = type;
	}

	public String getState() {
		return State;
	}

	public void setState(String state) {
		State = state;
	}

	public String getLevel() {
		return Level;
	}

	public void setLevel(String level) {
		Level = level;
	}

	public String getIsDeveloperSelfTest() {
		return IsDeveloperSelfTest;
	}

	public void setIsDeveloperSelfTest(String isDeveloperSelfTest) {
		IsDeveloperSelfTest = isDeveloperSelfTest;
	}

	public String getSteps() {
		return Steps;
	}

	public void setSteps(String steps) {
		Steps = steps;
	}

	public String getDirectory() {
		return Directory;
	}

	public void setDirectory(String directory) {
		Directory = directory;
	}

	public String getPreTest() {
		return PreTest;
	}

	public void setPreTest(String preTest) {
		PreTest = preTest;
	}

	public String getExResult() {
		return ExResult;
	}

	public void setExResult(String exResult) {
		ExResult = exResult;
	}
}
